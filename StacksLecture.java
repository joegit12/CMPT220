import java.util.Stack;

public class StacksLecture {
    public static void main(String[] args) throws exception {
        Stack<String> stax = new Stack<>();
        stax.push("j");
        stax.push("o");
        stax.push("s");
        stax.push("p");
        stax.push("h");
        stax.peek();
        System.out.println(stax.peek());



    }



}
